    private void BrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BrowseActionPerformed
        JFileChooser chooser= new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(".txt","txt");
        chooser.setFileFilter(filter);
        int choice = chooser.showOpenDialog(file);
        if (choice != 0) return;
        File chosenFile = chooser.getSelectedFile();
        file.setText(chosenFile.toString());
         String s=chosenFile.toString();
        String extension = s.substring(s.indexOf("."));
        if(extension.equals(".txt"))
        {
            error.setVisible(false);
            calcuate.setEnabled(true);
            try {
                 br = new BufferedReader( new InputStreamReader( new FileInputStream(chosenFile) ) );
            } catch (NullPointerException | FileNotFoundException ex) {
                calcuate.setEnabled(false);
                error.setText("ERROR 404! File not found");
                error.setVisible(true);
            }
                 FileContent = new StringBuffer();
                String line;
            try {
                 while ( ( line = br.readLine() ) != null )
                FileContent.append( line );
                 if(FileContent.length()<1)
                 {
                     
                    calcuate.setEnabled(false);
                    error.setText("Error! Empty file");
                    error.setVisible(true);
                 }
            } catch (IOException ex) {
            }
                content = FileContent.toString();
        }
        else
        {
            error.setText("Error! Invalid file format");
            error.setVisible(true);
            calcuate.setEnabled(false);
        }
    }//GEN-LAST:event_BrowseActionPerformed

