    public void Entropy_(String x)
    {
        x = x.replace(System.getProperty("line.separator"), "");
        double total=0;
        for (int i=0;i<x.length();i++)
        {
            list1.add(x.substring(i, i+1));
            if(only_1.indexOf(list1.get(i))==-1)
            {
                only_1.add(list1.get(i));
            }
        }
        for (int i=0;i<only_1.size();i++)
        {
            jTextArea1.append(only_1.get(i)+"\n");
        }
        for(int i=0;i<only_1.size();i++)
        {
            list2.add(probablilty(counter(list1,only_1.get(i))));
            jTextArea2.append(list2.get(i).toString()+"\n");
        }
        for (int i=0;i<list2.size();i++)
        {
            list3.add(log2(1/(double)list2.get(i)));
            jTextArea3.append(list3.get(i).toString()+"\n");
        }
        for (int i=0;i<list3.size();i++)
        {
            double temp=0;
            temp=(double)list2.get(i)*(double)list3.get(i);
            total+=temp;
            jTextArea4.append(String.valueOf(temp)+"\n");
        }
        jTextArea4.append("Total: "+String.valueOf(total));
    }
